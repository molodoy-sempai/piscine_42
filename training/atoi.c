#include <stdio.h>
#include <unistd.h>

void	ft_putstr(char *s)
{
	int k;

	k = 0;
	while(s[k])
	{
		write(1, &s[k], 1);
			k++;
	}
}

int	fti_atoi(char *str)
{
	int	result;
	int	sign;

	result = 0;
	sign = 1;
	while (*str == ' ' || *str == '\t' || *str == '\n' || *str == '\r' || *str == '\v' || *str == '\f')
		str++;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + *str - '0';
		str++;
	}
	return (result * sign);
}

int	main(int argc, char *argv[])
{
	printf("%d", (fti_atoi(argv[1])));
}
