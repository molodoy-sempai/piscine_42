#include <unistd.h>

int	ft_fn(int i)
{
	if ( i <= 5)
	{
		i++;
		write(1, "D", 1);
		ft_fn(i);
		write(1, "F", 1);
		return (0);
	}
	else
	{
		return (0);
	}
}

